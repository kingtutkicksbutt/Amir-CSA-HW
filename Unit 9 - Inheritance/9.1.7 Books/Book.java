public class Book
{
    private int pages;
    private String name;
    
    public void setPages(int pages) {
        this.pages = pages;
    }
    
    public int getPages() {
        return pages;
    }
    
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}